package ch03_1_operator_expression;


public class OperatorExpression {

	public static void main(String[] args) {

		int x = -100;
		int r1 = +x;
		int r2 = -x;
		System.out.println("r1=" + r1);
		System.out.println("r2=" + r2);
		
		byte b = 100;
		int r3 = -b;
		System.out.println("r3=" + r3);
		
		
	}

}
